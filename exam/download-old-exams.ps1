$total = $subjects.Count
$i = 0
$errors = @()

foreach ($sub in $subjects) {
    $i++
    $parts = $sub -split "_"
    $code = $parts[0]; $c = $parts[1]; $s = $parts[2]
    $outFile = "$outDir\Q_${sub}.pdf"

    if (Test-Path $outFile) {
        Write-Host "[$i/$total] 已存在，跳過: $sub" -ForegroundColor Gray
        continue
    }

    $url = "${baseUrl}?t=Q&code=${code}&c=${c}&s=${s}&q=1"
    try {
        Invoke-WebRequest -Uri $url -OutFile $outFile -UseBasicParsing -TimeoutSec 30
        $size = (Get-Item $outFile).Length
        Write-Host "[$i/$total] 下載完成: $sub ($([math]::Round($size/1024))KB)" -ForegroundColor Green
    } catch {
        Write-Host "[$i/$total] 失敗: $sub - $_" -ForegroundColor Red
        $errors += $sub
    }
    Start-Sleep -Milliseconds 500   # 避免伺服器限速
}

Write-Host "`n=== 完成 ===" -ForegroundColor Cyan
Write-Host "成功: $($total - $errors.Count)/$total"
if ($errors.Count -gt 0) {
    Write-Host "失敗清單:" -ForegroundColor Yellow
    $errors | ForEach-Object { Write-Host "  $_" }
}
